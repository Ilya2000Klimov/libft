#include "libft.h"

int		ft_intlength(int n)
{
	int	i;
	int sign;

	i = 0;
	sign = 0;
	if (n <= 0)
		sign = 1;
	while (n != 0)
	{
		i++;
		n = n / 10;
	}
	return (i + sign);
}
